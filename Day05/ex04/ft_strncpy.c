char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int t;

	t = 0;
	while (src[t] != '\0' && t < n)
	{
		dest[t] = src[t];
		t++;
	}
	while (t < n)
	{
		dest[t] = '\0';
		t++;
	}
	return (dest);
}
