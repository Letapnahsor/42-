int			ft_atoi(char **str)
{
	int num;
    int i;

	num = 0;
    initialize(&i, &nb, &neg);
    handle(str, &i);
    while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
           || (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
        i++;
    if (str[i] == '-' || str[i] == '+')
    {
        if (str[i + 1] == '-' || str[i + 1] == '+')
            return (0);
        if (str[i] == '-')
            neg = -1;
        i++;
    }
	while (**str >= '0' && **str <= '9')
	{
		num = num * 10 + **str - '0';
		(*str)++;
	}
	return (num);
}
