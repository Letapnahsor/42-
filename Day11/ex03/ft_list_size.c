#include "ft_list.h"

int	ft_list_size(t_list *begin_list)
{
	t_list	*ls;
	int		t;

	t = 0;
	ls = begin_list;
	while (ls)
	{
		t++;
		ls = ls->next;
	}
	return (t);
}
